-- CURSOR->POINTER: FETCHES ROWS ONE BY ONE


-- SIMPLE CURSOR:
DO $$ 
DECLARE 
REC RECORD;
CUR CURSOR FOR SELECT * FROM FILM;	-- DECLARING A CURSOR 'CUR'/ UNBOUNDED CURSOR
BEGIN
	OPEN CUR;		-- OPENING THE CURSOR

	LOOP
		FETCH CUR INTO REC; 	-- FETCH DATA USING CUR

		EXIT WHEN NOT FOUND;	-- CHECKING IF ALL ROWS ARE FETCHED

		RAISE NOTICE 'TITLE: %',REC.TITLE;

	END LOOP;

	CLOSE CUR;					-- CLOSING THE CURSOR
END $$;


-- PARAMETERISED CURSOR:
DO $$
DECLARE 
CUR CURSOR (YR INTEGER) FOR 
SELECT *FROM FILM WHERE RELEASE_YEAR=YR; -- BOUNDED CURSOR
REC RECORD;
BEGIN
		OPEN CUR(1972);

		LOOP 
			FETCH CUR INTO REC;

			EXIT WHEN NOT FOUND;
	
			RAISE NOTICE 'TITLE: %',REC.TITLE;

		END LOOP;

		CLOSE CUR;
END $$;


DO $$
DECLARE 
CUR CURSOR FOR SELECT *FROM FILM;
REC RECORD;
BEGIN 
	OPEN CUR;
	
	FETCH FIRST FROM CUR INTO REC;	-- FETCHES FIRST ROW
	RAISE NOTICE 'TITLE: %',REC.TITLE;

	FETCH LAST FROM CUR INTO REC;	-- FETCHES LAST ROW
	RAISE NOTICE 'TITLE: %', REC.TITLE;

	FETCH PRIOR FROM CUR INTO REC;	-- FETCHES THE ROW JUST ABOVE THE PRIVIOUS ROW FETCHED
	RAISE NOTICE 'TITLE: %',REC.TITLE;

	FETCH ABSOLUTE 3 FROM CUR INTO REC;	-- FETCHES ROW #03
	RAISE NOTICE 'TITLE: %',REC.TITLE;

	FETCH RELATIVE 1 FROM CUR INTO REC; -- FETCHES ONE ROW AHEAD OF THE CURRENT POSITION
	RAISE NOTICE 'TITLE: %',REC.TITLE;

	UPDATE FILM SET RELEASE_YEAR=1960
	WHERE CURRENT OF CUR;			-- UPDATING RECORD USING CURSOR
	
	CLOSE CUR;
END $$;

