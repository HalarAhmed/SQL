-- FUNCTION DEFINITION:
CREATE OR REPLACE FUNCTION FILM_COUNT(LEN_FROM INTEGER,LEN_TO INTEGER)
RETURNS INTEGER
LANGUAGE PLPGSQL
AS $$
DECLARE
TOTAL INTEGER := 0;
BEGIN
	SELECT COUNT(*) INTO TOTAL 
	FROM FILM WHERE DURATION BETWEEN LEN_FROM AND LEN_TO;

	RETURN TOTAL;
END $$;

SELECT FILM_COUNT(LEN_FROM:=90,LEN_TO:=190);
SELECT FILM_COUNT(90,190);
