CREATE OR REPLACE PROCEDURE ADD_AMOUNT(
SENDER INT,
RECIEVER INT,
AMOUNT DECIMAL(10,4)
)
LANGUAGE PLPGSQL
AS $$
BEGIN
	UPDATE ACCOUNTS SET BALANCE=BALANCE-AMOUNT 
	WHERE ID=SENDER;
	
	UPDATE ACCOUNTS SET BALANCE=BALANCE+AMOUNT
	WHERE ID=RECIEVER;

	COMMIT;
END $$;

