-- 2. SAVE THE OLD VALUES WHEN DATA CHANGES:
DROP TABLE IF EXISTS employees;

CREATE TABLE employees (
    id INT GENERATED ALWAYS AS IDENTITY,
    name VARCHAR(50) NOT NULL,
    salary NUMERIC NOT NULL,
    PRIMARY KEY(id)
);

DROP TABLE IF EXISTS employee_audit;

CREATE TABLE employee_audit (
    id SERIAL,
    employee_id INT,
    old_salary NUMERIC,
    changed_on TIMESTAMP
);

CREATE OR REPLACE FUNCTION LOG_SALARY_UPDATE()
RETURNS TRIGGER
LANGUAGE PLPGSQL
AS $$
BEGIN
	IF NEW.SALARY<>OLD.SALARY THEN
		INSERT INTO EMPLOYEE_AUDIT(EMPLOYEE_ID,OLD_SALARY,CHANGED_ON) 
		VALUES(OLD.ID,OLD.SALARY,NOW());
	END IF;

	RETURN NEW;
END $$;

-- BINDING THE TRIGGER:
CREATE TRIGGER SALARY_UPDATE_LOG
AFTER UPDATE 
ON EMPLOYEES
FOR EACH ROW
EXECUTE PROCEDURE LOG_SALARY_UPDATE();

UPDATE employees
SET salary = 80000
WHERE id = 1;



-- 3. SAVING DELETED RECORDS:
DROP TABLE IF EXISTS deleted_employees;

CREATE TABLE deleted_employees (
    id INT,
    name VARCHAR(50),
    salary NUMERIC,
    deleted_at TIMESTAMP
);

CREATE OR REPLACE FUNCTION BACKUP_DELETED_EMPLOYEES()
RETURNS TRIGGER
LANGUAGE PLPGSQL
AS $$
BEGIN
	INSERT INTO deleted_employees(ID,NAME,SALARY,DELETED_AT) 
	VALUES (OLD.ID,OLD.NAME,OLD.SALARY,NOW());

	RETURN OLD;
END $$;


-- BINDING THE TRIGGER:
CREATE TRIGGER EMP_DEL_BACKUP
AFTER DELETE 
ON EMPLOYEES
FOR EACH ROW
EXECUTE PROCEDURE BACKUP_DELETED_EMPLOYEES();

