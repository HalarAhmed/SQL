DROP TABLE IF EXISTS staff;

CREATE TABLE staff (
    id INT GENERATED ALWAYS AS IDENTITY,
    name VARCHAR(50),
    username VARCHAR(50)
);

-- 1. DATA VALIDATION BEFORE INSERTION:
CREATE OR REPLACE FUNCTION CHECK_USERNAME()
RETURNS TRIGGER
LANGUAGE PLPGSQL
AS $$
BEGIN
	IF NEW.USERNAME IS NULL OR LENGTH(NEW.USERNAME)<8 THEN -- 'NEW' HERE MEANS THE VALUE THAT USER HAS GIVEN
		RAISE EXCEPTION 'USERNAME MUST BE ATLEAST 8 CHARS LONG';
	END IF;

	RETURN NEW;
END $$;

-- BINDING TRIGGER:
CREATE TRIGGER USERNAME_CHECK
BEFORE INSERT	-- CHECK BEFORE INSERTING
ON STAFF
FOR EACH ROW 
EXECUTE PROCEDURE CHECK_USERNAME();



